import java.io.*;
import java.util.*;
public class Main {
    public static void main(String[] args)throws Exception{
        BufferedReader br=new BufferedReader(new InputStreamReader(System.in));
        System.out.println(br.readLine());    //reads the input as string
        int t=Integer.parseInt(br.readLine()); //convert input string to integer
        System.out.println(t); //print integer output
    }
}
